function toCountWhile() {
  let n = 0;
  while (n <= 6) {
    console.log(`${n} + while loop`);
    n += 1;
  }
}

toCountWhile();

function toCountDoLoop() {
  let n = 0;
  do {
    console.log(`${n} + do while loop`);
    n += 1;
  } while (n <= 6);
}

toCountDoLoop();
